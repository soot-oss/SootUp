    public void intArrays(){
        int[][] anArrayOfInts = {{1, 2, 3},{5, 6},{7, 8, 9}};
    }

    public void byteArrays(){
        byte[][] anArrayOfBytes = {{4, 5},{2}};

    }
    public void shortArrays(){
        short[][] anArrayOfShorts = {{10, 20, 30},{40}};

    }
